package com.tianyi.bph.query.report;

public class CaseInfo {
	/**
	 ** 案情处理地点经度
	 **/
	private String lnt;
	/**
	 ** 案情处理地点维度
	 **/
	private String lat;
	/**
	 ** 案情编号
	 **/
	private String jjdCode;
	public String getLnt() {
		return lnt;
	}
	public void setLnt(String lnt) {
		this.lnt = lnt;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getJjdCode() {
		return jjdCode;
	}
	public void setJjdCode(String jjdCode) {
		this.jjdCode = jjdCode;
	}
}
