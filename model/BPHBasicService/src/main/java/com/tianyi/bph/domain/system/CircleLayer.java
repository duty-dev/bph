package com.tianyi.bph.domain.system;

/**
 * 圈层
 * 
 * @author Administrator
 *
 */
public class CircleLayer {

	private int id;			//圈层id
	private String name;	//圈层名称
	private String note;	//描述
	private String displayProperty;	//显示属性
	private String mapProperty;	//地图属性
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDisplayProperty() {
		return displayProperty;
	}
	public void setDisplayProperty(String displayProperty) {
		this.displayProperty = displayProperty;
	}
	public String getMapProperty() {
		return mapProperty;
	}
	public void setMapProperty(String mapProperty) {
		this.mapProperty = mapProperty;
	}
	
}
